#include<stdio.h>

/********************
 *
 * p == &a
 * p + 1 == 1 + p
 * p + 1 == &p[1]
 * *p == p[0] == a
 * p[n] == *(p + n)
 *
 ********************/

int main() {
    int arr[] = {0, 1, 2, 3, 4, 5, 6};
    int *p = arr;
    for (int i = 0; i < 3; i++) {
        printf("%d\n", (i + 5)[&p[1] - 2]); // &p[1] - 2 == p + 1 - 2 == p - 1;  (i + 5)[p - 1] == *(i + 5 + p - 1) == p[i + 4];
    }
    return 0;
}
